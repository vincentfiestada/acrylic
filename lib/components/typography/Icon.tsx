export interface IconComponent
	extends React.FC<IImageIconProps | IEmblemIconProps> {}

const Icon: IconComponent = (props: IImageIconProps | IEmblemIconProps) => {
	if (isImageIconProps(props)) {
		const { src, classes, children, ...otherProps } = props;

		return (
			<img
				className={classNames("acr-icon", classes)}
				src={src}
				{...otherProps}
			/>
		);
	} else {
		const { classes, children, ...otherProps } = props;

		return (
			<span className={classNames("acr-icon", classes)} {...otherProps}>
				{toUnicodeString(children)}
			</span>
		);
	}
};
export default Icon;

/**
 * Type guard to check if props implement the IImageIconProps interface
 * @param props the props to check
 * @private
 */
function isImageIconProps(props: any): props is IImageIconProps {
	return props && typeof props.src === "string";
}

/**
 * Get a string from a character code
 * @param chars the unicode character code as a string or number
 * @private
 */
function toUnicodeString(chars: string | number): string {
	if (typeof chars === "string") {
		return String.fromCharCode(parseInt(chars, 16));
	} else {
		return String.fromCharCode(chars);
	}
}
